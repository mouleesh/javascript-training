//Conditional Statement if, else if, else.
let name = "virat"

if(name !== "virat"){
  console.log("Happy Birthday " + name)
} else if(name === "dhoni") {
  console.log("Happy Birthday Dhoni")
} else {
  console.log("Happy Birthday Somebody Else")
}



